Crea una página Web que muestre el siguiente formulario. La página deberá
llamarse datos.php.

Al pulsar el botón “Calcular”, la página debe comprobar que
se ha introducido un valor en todos los campos de texto
y que dichos valores son en todos los casos números.

Si algún campo no cumple los requisitos deberá mostrarse con fondo rojo y un mensaje
al lado que indique “Campo Vacio” o “Valor no válido” según sea el caso.

Si todos los campos cumplen la validación deberá redireccionarse al usuario a la página
resultados.php donde se han de mostrar los siguientes valores en función de los datos
introducidos por el usuario:

• El valor más grande.
• El valor más pequeño.
• El sumatorio de todos los valores introducidos.
• La media.

PSEUDO CODIGO

1. Crear un formulario con los campos de texto necesarios para introducir los valores.
2. Crear un botón de tipo submit para enviar los datos con el nombre: Calular.
3. Crear un archivo llamado datos.php que contenga el formulario.
4. Crear un archivo llamado resultados.php que contenga el código para mostrar los resultados.
5. En el archivo datos.php, comprobar que se han introducido valores en todos los campos de texto.
6. En el archivo datos.php, comprobar que los valores introducidos son números.
7. En el archivo datos.php, si algún campo no cumple los requisitos, mostrarlo con fondo rojo y un mensaje al lado que indique “Campo Vacio” o “Valor no válido” según sea el caso.
8. En el archivo datos.php, si todos los campos cumplen la validación, redireccionar al usuario a la página resultados.php.
9. En el archivo resultados.php, mostrar los siguientes valores en función de los datos introducidos por el usuario:
• El valor más grande.
• El valor más pequeño.
• El sumatorio de todos los valores introducidos.
• La media.